public class security {
    
    
    public static String encodestring(String passwd,long sorce){
        
        try {
            MessageDigest digest=MessageDigest.getInstance("SHA1");
          
            String passwdtohash=passwd+sorce;
            byte[] passwdhash=digest.digest(passwd.getBytes());
            Base64.Encoder encoder=Base64.getEncoder();
            String hash=encoder.encodeToString(passwdhash)+","+sorce;
            return hash;
        } catch (NoSuchAlgorithmException ex) {
            return null;
        }

    }//encode string
    

    
}//security class
